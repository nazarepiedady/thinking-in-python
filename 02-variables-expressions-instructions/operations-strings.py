'''
it cannot possible use mathematical operations
with strings except `addition` and `multiplication`
'''

# `addition` example
# use it to concatenate strings
throat = 'throat'
warbler = 'warbler'
throatwarbler = throat + warbler

print(throatwarbler)

# `multiplication` example
# use it to repeat the string
spam = 'spam'
spam_spam_spam = spam * 3

print(spam_spam_spam)
