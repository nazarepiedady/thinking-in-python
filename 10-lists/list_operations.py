numbers = [1, 2, 3]
more_numbers = [4, 5, 6]

# concatenate lists
combined_numbers = numbers + more_numbers
print(combined_numbers)

# repeat the elements inside lists
repeated_numbers = numbers * 3
print(repeated_numbers)


# sum all numeric elements in the list
result_sumed_number = sum(numbers)
print(result_sumed_number)
